var x = 5, y = 6, z = 12;

// Arithmetic operators

console.log(x+y+z);
console.log(x*y-z);
console.log(x/y);
console.log(z%y);
console.log(x++);
console.log(++x);
console.log(y--);
console.log(--y);
console.log(x**y);

// String Operators

console.log("Hello" + " " + "World");
console.log("Hello" + 5);

// Relational Operators

console.log(x > 5);
console.log(x <= 10);
console.log(y == 8);

console.log(x == "5");
console.log(x === "5");

console.log(y != 8);
console.log(y !== 8);

// Logical Operators

console.log((x == 5) || (x == 3));
console.log((x == 5) && (x == 3));
console.log(!(x==5));

// Bitwise Operators

console.log(x & 1);
console.log(x | 1);
console.log(~x);
console.log(x>>1);
console.log(x<<1);
